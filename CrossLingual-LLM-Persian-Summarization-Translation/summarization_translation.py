inputs2 = tokenizer_3(prompt_summarize, return_tensors="pt", truncation=True, max_length=512)

summary_ids = model_3.generate(
    **inputs2,
    max_new_tokens=80,
    num_beams=5,
    no_repeat_ngram_size=2,
    early_stopping=True
)
summary = tokenizer_3.decode(summary_ids[0], skip_special_tokens=True)
print("Summary (Mistral):", summary)

# ---- ترجمه به انگلیسی ----
prompt_translate = "Translate the following Persian dialogue into English:\n" + text

inputs = tokenizer_3(prompt_translate, return_tensors="pt", truncation=True, max_length=512)

translation_ids = model_3.generate(
    **inputs,
    max_new_tokens=100,
    num_beams=5,
    no_repeat_ngram_size=2,
    early_stopping=True
)
translation = tokenizer_3.decode(translation_ids[0], skip_special_tokens=True)
print("Translation (Mistral):", translation)