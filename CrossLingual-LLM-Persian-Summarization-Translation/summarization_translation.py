from transformers import AutoTokenizer, AutoModelForSeq2SeqLM

model_name='google/flan-t5-base'
model = AutoModelForSeq2SeqLM.from_pretrained(model_name)

tokenizer = AutoTokenizer.from_pretrained(model_name, use_fast=True)

"""##Test the tokenizer encoding and decoding a simple sentence:"""

sentence = "علی: سلام، داداش! چطوری؟"

sentence_encoded = tokenizer(sentence, return_tensors='pt')

sentence_decoded = tokenizer.decode(
        sentence_encoded["input_ids"][0],
        skip_special_tokens=True
    )

print('ENCODED SENTENCE:')
print(sentence_encoded["input_ids"][0])
print('\nDECODED SENTENCE:')
print(sentence_decoded)

"""The model doesn't understand persian character at all. It seems that FLAN-T5 is trained mainly on English.
Its SentencePiece tokenizer doesn’t recognize Persian characters, so it turns them into generic unknown tokens. I use a multilingual model trained on persian too named "**facebook/mbart-large-50-many-to-many-mmt**"
"""

model_name_2 = "facebook/mbart-large-50-many-to-many-mmt"
tokenizer_2 = AutoTokenizer.from_pretrained(model_name_2, use_fast = True)
model_2 = AutoModelForSeq2SeqLM.from_pretrained(model_name_2)
tokenizer_2.tgt_lang = 'fa_IR'

sentence = "علی: سلام، داداش! چطوری؟"

sentence_encoded = tokenizer_2(sentence, return_tensors='pt')

sentence_decoded = tokenizer_2.decode(
        sentence_encoded["input_ids"][0],
        skip_special_tokens=True
    )

print('ENCODED SENTENCE:')
print(sentence_encoded["input_ids"][0])
print('\nDECODED SENTENCE:')
print(sentence_decoded)

"""We can see **facebook/mbart-large-50-many-to-many-mmt** will correctly tokenize Persian text and decode it back to the same string. Now it's time to explore how well this base LLM summarizes a dialogue without any prompt engineering."""

prompt = f"""
 Summarize the following Persian text in Persian:
علی: سلام، داداش! چطوری؟
محمد: سلام علی! خوبم، تو چطوری؟
علی: منم خوبم. این هفته قراره بریم فوتبال؟
محمد: آره، می‌خوام برم. ساعت چند؟
علی: ساعت ۵ تو پارک محل. بیا با هم بریم.
محمد: اوکی، منتظرت می‌مونم.
علی: بعد بازی می‌خوای بریم بستنی؟
محمد: آره، خیلی خوب میشه!
علی: پس شنبه همدیگه رو می‌بینیم.
محمد: حتماً، خداحافظ!
"""
# Set the beginning-of-sentence (BOS) token to Persian ("fa_IR") so that the model generates output in Persian.
model_2.config.forced_bos_token_id = tokenizer_2.lang_code_to_id["fa_IR"]

# Input constructed prompt instead of the dialogue.

inputs = tokenizer_2(prompt, return_tensors="pt")
output = tokenizer_2.decode(
        model_2.generate(
            inputs["input_ids"],
            max_new_tokens=100,
        )[0],
        skip_special_tokens=True
    )

print(f'Zero-Shot Summarization:\n{output}\n')

"""# Task 2 : Translation of the Dialogue"""

# Set source and target languages
tokenizer_2.src_lang = "fa_IR"   # Input is Persian
tokenizer_2.tgt_lang = "en_XX"   # Output should be English

# Persian dialogue
text = """
علی: سلام، داداش! چطوری؟
محمد: سلام علی! خوبم، تو چطوری؟
علی: منم خوبم. این هفته قراره بریم فوتبال؟
محمد: آره، می‌خوام برم. ساعت چند؟
علی: ساعت ۵ تو پارک محل. بیا با هم بریم.
محمد: اوکی، منتظرت می‌مونم.
علی: بعد بازی می‌خوای بریم بستنی؟
محمد: آره، خیلی خوب میشه!
علی: پس شنبه همدیگه رو می‌بینیم.
محمد: حتماً، خداحافظ!
"""

# 🔹 Prompt to translate the dialogue
prompt = f"Translate the following Persian dialogue to English:\n{text}"

# Tokenize input
inputs = tokenizer_2(prompt, return_tensors="pt")

# Set the BOS token to English so output starts in English
model_2.config.forced_bos_token_id = tokenizer_2.lang_code_to_id["en_XX"]

# Generate translation
output = model_2.generate(inputs["input_ids"], max_new_tokens=100)

# Decode and print
translation = tokenizer_2.decode(output[0], skip_special_tokens=True)
print("English Translation:\n", translation)

