"""3.keep ONLY the last translation

MBART is a sequence-to-sequence machine translation model.It is not trained to follow instructions. Instead, it learned the pattern and began to reproduce the entire structure, including:the instruction, the example sentences, the “English:” labels, and then the new translation.

**Fixing the issue (extracting only the final translation)**

To solve this, I implemented a post-processing step that removes everything except the last Persian sentence corresponding to the test input.
After extracting only the actual translation, I re-evaluated the model.

This allowed me to compute the BLEU score correctly while still using the few-shot prompt.
"""

import re

def extract_last_translation(text):
    """
    Clean MBART output by removing repeated prompt and examples,
    returning only the translation of the target sentence.
    """
    text = text.strip()

    # First, try to remove repeated prompts using common Persian cues
    cues = [
        r"زبان پارسی\s*:\s*",
        r"پارسی\s*:\s*",
        r"به زبان پارسی\s*[:]*\s*",
        r"این جمله ها را به زبان پارسی ترجمه کنید\s*[:]*\s*",
    ]

    last_pos = -1
    for cue in cues:
        match = list(re.finditer(cue, text))
        if match:
            last_pos = max(last_pos, match[-1].end())

    if last_pos != -1:
        text = text[last_pos:].strip()

    # Then, split on the Persian word "انگلیسی:" to catch any remaining repeated examples
    parts = text.split("انگلیسی:")
    if len(parts) > 1:
        text = parts[-1].strip()

    # Fallback: take content after last colon if necessary
    elif ":" in text:
        text = text.split(":")[-1].strip()

    return text

#Updated translation function
def translate_prompt_icl(input_sentence):
    prompt = build_prompt(input_sentence, example_pairs)
    inputs = tokenizer(prompt, return_tensors="pt")

    generated_tokens = model.generate(
        **inputs,
        forced_bos_token_id=tokenizer.lang_code_to_id[tokenizer.tgt_lang],
        max_new_tokens= 120,
        num_beams=5,
        repetition_penalty=2.0,
    )

    output = tokenizer.decode(generated_tokens[0], skip_special_tokens=True)
    output = extract_last_translation(output)  # <-- strip prompts
    return output

